-- [[projectrc]]
-- Per-project configuration, only runs when "projectrc.lua" is found in the root dir.
-- Plugin requirements:
-- * neoterm
-- * (optional) nvim-notify
vim.api.nvim_create_autocmd("VimEnter", {
	desc = "Load per project config",
	once = true,
	group = vim.api.nvim_create_augroup('projectrc', { clear = true }),
	callback = function()
		-- local ok, projectrc = pcall(require, vim.fn.getcwd() .. "/projectrc.lua")
		local ok, projectrc = pcall(require, "projectrc")
		if ok then
			local function execute_script(cmd)
				vim.g.neoterm_default_mod = "botright vertical"
				vim.api.nvim_command(":1T cls")
				vim.api.nvim_command(":1T " .. cmd)
			end

			local tasks = projectrc["tasks"]
				if tasks ~= nil then
				for i, entry in ipairs(tasks) do
					vim.keymap.set("n", entry.key, function() execute_script(entry.cmd) end, { silent = true })
				end
			end

			vim.notify("projectrc successfully loaded.", vim.log.levels.INFO, { title = "projectrc" })
		end
	end,
})

